    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        Equipos equipoaeditar = new Equipos();
        equipoaeditar.setNombre(tfnombre.getText());
        equipoaeditar.setRanking(tfranking.getText());
        equipoaeditar.setGrupo(tfgrupo.getText());
        equipoaeditar.setNumCopas(tfcopas.getText());
        equipoaeditar.setPartido1(tf01.getText());
        equipoaeditar.setPartido2(tf02.getText());
        equipoaeditar.setPartido3(tf03.getText());

        File archivoEquipos = new File(System.getProperty("user.dir") + "/src/archivos/Mundial.csv");

        // actualizamos valores nuevos
        int a = listEquipos.getSelectedIndex();
        System.out.println("AAAAAA " + a);
        equipos.set(a, equipoaeditar);

        try {
            FileWriter w = new FileWriter(archivoEquipos);
            BufferedWriter bw = new BufferedWriter(w);
            PrintWriter wr = new PrintWriter(bw);
            System.out.println("TOTAL EQUIPO " + equipos.size());
            for (int i = 0; i < equipos.size(); i++) {

                // en caso de ser la ultima linea no agregar salto de linea
                if (i == (equipos.size() - 1)) {
                    System.out.println("---------- EQUIPO " + i + " SI " + equipos.get(i).getNombre());
                    wr.write(equipos.get(i).getNombre() + ","
                            + equipos.get(i).getRanking() + ","
                            + equipos.get(i).getGrupo() + ","
                            + equipos.get(i).getNumCopas() + ","
                            + equipos.get(i).getPartido1()
                            + "," + equipos.get(i).getPartido2()
                            + "," + equipos.get(i).getPartido3());

                } else {
                    System.out.println("---------- EQUIPO " + i + " NO " + equipos.get(i).getNombre());

                    wr.write(equipos.get(i).getNombre() + ","
                            + equipos.get(i).getRanking() + ","
                            + equipos.get(i).getGrupo() + ","
                            + equipos.get(i).getNumCopas() + ","
                            + equipos.get(i).getPartido1()
                            + "," + equipos.get(i).getPartido2()
                            + "," + equipos.get(i).getPartido3()
                            + "\n");
                }
            }
            wr.close();
            bw.close();

        } catch (IOException e) {
            e.printStackTrace();
        }

// TODO add your handling code here:
    }//GEN-LAST:event_btnguardarActionPerformed

