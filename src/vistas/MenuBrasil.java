    public MenuBrasil() {
        initComponents();
        setBackground(Color.white);

        try {

            CsvReader equipos_import = new CsvReader(System.getProperty("user.dir") + "/src/archivos/Mundial.csv");

            while (equipos_import.readRecord()) {
                String nombre = equipos_import.get(0);
                String ranking = equipos_import.get(1);
                String grupo = equipos_import.get(2);
                String copas = equipos_import.get(3);
                String partido1 = equipos_import.get(4);
                String partido2 = equipos_import.get(5);
                String partido3 = equipos_import.get(6);
                equipos.add(new Equipos(nombre, ranking, grupo, copas, partido1, partido2, partido3));
                System.out.println("TOTAL INICIAL " + equipos.size() + nombre);
            }

            equipos_import.close();

            int cont = 1;
            for (Equipos eq : equipos) {

//	            System.out.println(cont + "" +eq.getNombre()+ " ; " + eq.getRanking()+ " ; "
//	            + eq.getGrupo()+ " ; " + eq.getNumCopas()+ " ; " + eq.getPartido1() + " ; " + eq.getPartido2() + " ; " + eq.getPartido3());
                cont++;
                modelo.addElement(eq.getNombre());
            }
            listEquipos.setModel(modelo);
        } catch (FileNotFoundException e) {
            e.printStackTrace();
        } catch (IOException e) {
            e.printStackTrace();
        }

    }

